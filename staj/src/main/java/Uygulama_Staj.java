    private void asalCarpanHesaplaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asalCarpanHesaplaActionPerformed
        int sayi;
        String ciktilar = "";
        sayi=Integer.valueOf(asalSayiGiris.getText());
        if(sayi<=1 || asalSayiGiris.getText()==null){
            JOptionPane.showMessageDialog(null, "Lütfen 1'den farklı pozitif bir tam sayı giriniz");
        }
        else{
            int i=2;
            int temp=sayi;
            while(i<=sayi){
                
                if(temp%i==0){
                    
                    ciktilar = ciktilar+i+"\n";
                    temp= temp/i;
                }
                else{
                    i++;
                }
            }
           
            asalCarpanCikti.setText(ciktilar);
            
        }
    }//GEN-LAST:event_asalCarpanHesaplaActionPerformed

