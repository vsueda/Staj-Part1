    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
      int sayi1, sayi2, i, obeb=1, okek=0;
      
      sayi1= Integer.valueOf(obebSayi1.getText());
      sayi2= Integer.valueOf(obebSayi2.getText());
      if(sayi1==0||sayi2==0 || obebSayi2.getText()==null || obebSayi1.getText()==null){
          JOptionPane.showMessageDialog(null, "Giridiğiniz sayılar pozitif tam sayı olmalı.");
      }
      else{
          
      
      for(i=1;i<=sayi1 && i<=sayi2; i++){
          if(sayi1%i==0 && sayi2%i==0){
              obeb = i;
              okek = (sayi1*sayi2)/obeb;
          }
      }
        
       
      obebCikti.setText(String.valueOf(obeb));
      okekCikti.setText(String.valueOf(okek));
      }
    }//GEN-LAST:event_button1ActionPerformed

